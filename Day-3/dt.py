# Data Types 
# i. Numeric 
# 1.Integer
a=10
print(a)
print(type(a))
# 2.Float
t = 99.99
print(t)
print(type(t))
# 3.Complex
c = 12+8j
print(c)
print(type(c))
# ii. Sequence
# 1.list
b=[1,3,3,2,4]
print(b)
print(type(b))
print(id(b))
# 2.tuple
b=(1,3,5,7,35,6,3)
print(b)
print(type(b))
print(id(b))
# iii. Mapping
# 1. set
b={1,9,5,7,4,6,3}
print(b)
print(type(b))
print(id(b))
# 2. dictinory
b={"name": "Ajay", "email": "ajay@gmail.com", "password": "123"}
print(b)
print(type(b))
print(id(b))
# iv. Bollen
b=True
print(b)
print(type(b))
# v. None type
b=None
print(b)
print(type(b))
